package com.mbednarz.ujopde.s5;

public class OpAry {

    public static void main(String[] args) {

        /*
        OPERATOR - to znak służacy

        OPERATORY ARYTMETYCZNE:
            +  :  dodawanie liczb oraz łaczenie string'ów
            -  :  odejmowanie
            *  :  mnożenie
            /  :  dzielenie

            =   :  przypisanie wartości
            +=  :
            -=  :
            /=  :
            *=  :
            %=  :

            %  :  reszta z dzielenia (dzielenie MODULO)

            ++  :  inkrementacja - powiększ o 1
            --  :  dekrementacja - zmniejsz o 1

            y++  :  POST INKREMENTACJA
            y--  :  POST dekrementacja

            ++y  :  PRE INKREMENTACJA
            --y  :  PRE dekrementacja
         */

        int a = 5;

        System.out.println(a--);
        System.out.println(a);
    }
}
