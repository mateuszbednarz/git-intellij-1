package com.mbednarz.ujopde.s5;

public class OpRel {

    public static void main(String[] args) {

        /*
            OPERATOR RELACYJNY (inaczej OPERATOR PORÓWNANIA):

            ==  :  porównanie dwóch wartości
            !=  :  porównienie czy wartości sa różne od siebie
            >   :  większe od
            <   :  mniejsze od
            >=  :  większe bądź równe
            <=  :  mniejsze bądź równe

        */

        int a = 5,
            b = 7;

        //boolean isTrue = 4==8;
        System.out.println(a > b);

    }

}
